final Map<IsoCode, PhoneCountry> _phoneCountriesByIso = <IsoCode, PhoneCountry>{
  for (final country in kPhoneCountries) country.isoCode: country,
};

final List<_PhoneCountrySearchEntry> _phoneCountrySearchIndex = kPhoneCountries
    .map((country) => _PhoneCountrySearchEntry(country))
    .toList(growable: false);

PhoneCountry phoneCountryByIso(IsoCode isoCode) {
  return _phoneCountriesByIso[isoCode] ?? kPhoneCountries.first;
}

List<PhoneCountry> searchPhoneCountries(String query) {
  final normalizedQuery = query.toLowerCase().trim();
  if (normalizedQuery.isEmpty) {
    return kPhoneCountries;
  }

  return _phoneCountrySearchIndex
      .where((entry) => entry.matches(normalizedQuery))
      .map((entry) => entry.country)
      .toList(growable: false);
}

class _PhoneCountrySearchEntry {
  _PhoneCountrySearchEntry(this.country)
      : normalizedSearchText =
            '${country.name} ${country.dialCode}'.toLowerCase();

  final PhoneCountry country;
  final String normalizedSearchText;

  bool matches(String query) {
    return normalizedSearchText.contains(query);
  }
}
